// 面试题4：二维数组中的查找
// 题目：在一个二维数组中，每一行都按照从左到右递增的顺序排序，每一列都按
// 照从上到下递增的顺序排序。请完成一个函数，输入这样的一个二维数组和一个
// 整数，判断数组中是否含有该整数。

#include <cstdio>

bool findOneInArray(int* matrix, int rows, int columns, int item) {
    bool found = false;
    
    if (matrix != nullptr && rows > 0 && columns > 0) {
        int row_indx = 0; 
        int col_indx = columns - 1;
        
        while (row_indx < rows && col_indx >= 0) {
            // locate the item from top right of the matrix
            int index_value = matrix[row_indx * columns + col_indx];
            if (index_value == item) {
                found = true;
                break;
            } else if (index_value < item)
                ++row_indx;
            else
                --col_indx;
        }
    }
    return found;
}

//==============================================================================
//  Test function
//==============================================================================

void Test(char* testName, int* matrix, int rows, int columns, int number, bool expected)
{
    if(testName != nullptr)
        printf("%s begins: ", testName);

    bool result = findOneInArray(matrix, rows, columns, number);
    if(result == expected)
        printf("Passed.\n");
    else
        printf("Failed.\n");
}

